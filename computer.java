    public void result()
    {   //這是等於
        if(jTextField1.getText().length()==0)
        {
            JOptionPane.showMessageDialog(this,"你沒有輸入數字");
        }
        else
        {
            next = jTextField1.getText();
            float p =Float.parseFloat(prev);
            float n =Float.parseFloat(next);
            float s = 0;
        
            switch(not)
            {
                case "+":
                s=p+n;
                break;
                
                case "-":
                s=p-n;
                break;
                
                case "*":
                s=p*n;
                break;
                
                case "/":
                s=p/n;
                break;
                
            }
        jTextField1.setText(Float.toString(s));
        }
    }
    
